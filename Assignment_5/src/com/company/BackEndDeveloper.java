package com.company;

public class BackEndDeveloper extends Employee{
    public int numberOfBackDev;
    public int salary;

    public void setNumberOfBackDev(int numberOfBackDev) {
        this.numberOfBackDev = numberOfBackDev;
    }

    public int getNumberOfBackDev() {
        return numberOfBackDev;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public int getSalary() {
        return salary;
    }
}
