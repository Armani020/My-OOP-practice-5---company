package com.company;

public class FrontEndDeveloper extends Employee{
    public int numberOfFrontDev;
    public int salary;

    public void setNumberOfFrontDev(int numberOfFrontDev) {
        this.numberOfFrontDev = numberOfFrontDev;
    }

    public int getNumberOfFrontDev() {
        return numberOfFrontDev;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public int getSalary() {
        return salary;
    }
}
